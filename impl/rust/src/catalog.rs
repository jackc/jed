/// A column definition: name, declared type, nullability, primary-key flag, default.
#[derive(Clone, PartialEq, Eq, Debug)]
pub struct Column {
    pub name: String,
    /// The column's declared type â€” a built-in scalar or a user-defined composite
    /// (spec/design/composite.md). The open `Type` wrapper (CLAUDE.md Â§4): scalar-only call sites
    /// read `ty.scalar()`; the value codec / resolver branch on `Type::Composite`.
    pub ty: Type,
    /// The `numeric(p,s)` type modifier for a decimal column, or `None` for a non-decimal
    /// column OR an unconstrained `numeric` (spec/design/decimal.md Â§2). A constrained
    /// decimal column coerces stored values to this precision/scale.
    pub decimal: Option<DecimalTypmod>,
    /// The `varchar(n)` max-length type modifier for a `text` column, or `None` for a
    /// non-text column OR an unbounded `text` (spec/design/types.md Â§15). `n` is counted in
    /// code points; an over-length assignment traps `22001`. Persisted as a `u32` in the
    /// column entry's typmod slot when `type_code == 4` (format_version 22).
    pub varchar_len: Option<u32>,
    pub primary_key: bool,
    /// A PRIMARY KEY column is implicitly NOT NULL.
    pub not_null: bool,
    /// The column's **constant** `DEFAULT` value, pre-evaluated and type-coerced at CREATE
    /// TABLE, or `None` if it has no default or an *expression* default (`default_expr`).
    /// `Some(Value::Null)` is an explicit `DEFAULT NULL`. Applied for an omitted column or a
    /// `DEFAULT` keyword at INSERT (spec/design/constraints.md Â§2).
    pub default: Option<Value>,
    /// The column's **expression** `DEFAULT` (a non-constant default like `uuidv7()` or
    /// `1 + 1`), or `None` if it has no default or a *constant* default (`default`). Mutually
    /// exclusive with `default`. Stored as expression text (re-rendered verbatim at every
    /// commit, like a `CHECK` â€” spec/fileformat/format.md) plus the parsed expression the write
    /// paths resolve and evaluate per row (spec/design/constraints.md Â§2).
    pub default_expr: Option<DefaultExpr>,
    /// Whether the column is a `GENERATED { ALWAYS | BY DEFAULT } AS IDENTITY` column, and which
    /// kind (spec/design/sequences.md Â§13). `None` is a non-identity column. An identity column
    /// also carries `not_null` + a `default_expr` (`nextval` of its owned sequence); the kind gates
    /// whether an explicit INSERT/UPDATE value is accepted (`428C9`). Persisted as two column flag
    /// bits (format_version 15: bit 4 `is_identity`, bit 5 `identity_always`).
    pub identity: Option<IdentityKind>,
    /// The column's **effective** collation â€” `None` is `C` (byte / code-point order, the unchanged
    /// fast path), `Some(name)` a loaded non-`C` collation (spec/design/collation.md Â§1). Frozen at
    /// CREATE TABLE: an explicit `COLLATE "name"` clause, else the per-database default collation as
    /// of creation (PG-matching â€” a column's collation is fixed at creation, never re-derived). Only
    /// a `text` column may carry one. A column reference's *implicit* collation in the derivation
    /// (`42P22`); persisted as column flag bit 6 + a trailing name (format_version 17).
    pub collation: Option<String>,
}

/// The kind of an IDENTITY column (spec/design/sequences.md Â§13). `Always` (`GENERATED ALWAYS`)
/// rejects an explicit INSERT/UPDATE value (`428C9`) unless overridden; `ByDefault` (`GENERATED BY
/// DEFAULT`) accepts one, behaving like `serial`. Persisted as flag bit 5 `identity_always`
/// (set â‡’ `Always`, clear â‡’ `ByDefault`), meaningful only with bit 4 `is_identity`.
#[derive(Clone, Copy, PartialEq, Eq, Debug)]
pub enum IdentityKind {
    Always,
    ByDefault,
}

/// A column's **expression** `DEFAULT` (spec/design/constraints.md Â§2): its persisted
/// expression text â€” written back verbatim at every commit so the catalog bytes are stable
/// (spec/fileformat/format.md "Check-expression text") â€” and the parsed expression the write
/// paths resolve (against an empty scope, no columns) and evaluate per inserted row. Modeled on
/// `CheckConstraint`.
#[derive(Clone, PartialEq, Eq, Debug)]
pub struct DefaultExpr {
    pub expr_text: String,
    pub expr: Expr,
}

/// One `CHECK` constraint: its (resolved, unique-per-table) name, its persisted expression
/// text â€” written back verbatim at every commit so the catalog bytes are stable
/// (spec/fileformat/format.md "Check-expression text") â€” and the parsed expression the
/// write paths resolve and evaluate per candidate row (spec/design/constraints.md Â§4).
#[derive(Clone, PartialEq, Eq, Debug)]
pub struct CheckConstraint {
    pub name: String,
    pub expr_text: String,
    pub expr: Expr,
}

/// The KIND of a secondary index (spec/design/gin.md): an ordered B-tree (the default) or a
/// GIN inverted index. Persisted as the v12 per-index `index_kind` byte
/// (spec/fileformat/format.md).
#[derive(Clone, Copy, PartialEq, Eq, Debug)]
pub enum IndexKind {
    /// An ordered B-tree over the key-column tuple â€” the original (and default) index.
    Btree = 0,
    /// A GIN inverted index: one entry per term extracted from the (array) column
    /// (spec/design/gin.md Â§4). This slice: a single integer-element array column (`array_ops`).
    Gin = 1,
    /// A GiST index â€” a bounding-predicate R-tree (spec/design/gist.md). GX1: the `range_ops`
    /// opclass over a single `range` column. The in-memory store holds one leaf-key entry per row
    /// (`encode_range_body(bound) â€– storage_key`, gist.rs); the on-disk form is the persisted
    /// R-tree (page types 5/6, `format_version` 20 â€” GX1b). Never `unique`.
    Gist = 2,
}

/// One index key element (spec/design/indexes.md Â§1): a bare column (by ordinal into the
/// table's columns) or an **expression** over the table's columns (`lower(email)`), carrying
/// its persisted canonical text and the re-parsed (unresolved) AST â€” the write/plan paths
/// re-resolve it against the table per statement, exactly as a CHECK is re-resolved. Only a
/// B-tree index may carry an expression element (GIN/GiST are single column, this slice).
#[derive(Clone, PartialEq, Eq, Debug)]
pub enum IndexKey {
    Column(usize),
    Expr(IndexKeyExpr),
}

/// An index expression key's persisted text + parsed AST (modeled on `CheckConstraint`).
#[derive(Clone, PartialEq, Eq, Debug)]
pub struct IndexKeyExpr {
    pub expr_text: String,
    pub expr: Expr,
}

impl IndexKey {
    /// The column ordinal if this is a plain column key, else `None` (an expression key).
    pub fn as_column(&self) -> Option<usize> {
        match self {
            IndexKey::Column(c) => Some(*c),
            IndexKey::Expr(_) => None,
        }
    }
}

/// One secondary index of a table (spec/design/indexes.md): its (relation-namespace) name
/// and its key elements in index-key order (columns and/or expressions, duplicates allowed â€”
/// PG). The index's B-tree lives in the snapshot's index-store map, keyed by the lowercased
/// name. A `unique` index enforces uniqueness over its key tuple (NULLS DISTINCT â€”
/// spec/design/indexes.md Â§8); it is what backs a `UNIQUE` constraint
/// (spec/design/constraints.md Â§5). `kind` selects ordered-B-tree vs GIN
/// (spec/design/gin.md); a GIN index is never `unique`.
#[derive(Clone, PartialEq, Eq, Debug)]
pub struct IndexDef {
    pub name: String,
    pub keys: Vec<IndexKey>,
    pub unique: bool,
    pub kind: IndexKind,
    /// A **partial** index's `WHERE predicate` (spec/design/indexes.md Â§9): only rows whose
    /// predicate is TRUE are indexed / constrained. `None` for an ordinary (full) index. Carries
    /// the persisted canonical text + the re-parsed (unresolved) AST â€” re-resolved against the
    /// table per statement, like an expression key ([`IndexKeyExpr`]). Partial indexes are B-tree
    /// only (a GIN/GiST index never carries one).
    pub predicate: Option<IndexKeyExpr>,
}

impl IndexDef {
    /// Whether every key element is a plain column (no expression key) â€” the common case, and
    /// the GIN/GiST invariant. Lets column-only code paths fast-check.
    pub fn all_columns(&self) -> bool {
        self.keys.iter().all(|k| matches!(k, IndexKey::Column(_)))
    }

    /// The key elements' column ordinals if every element is a plain column, else `None` (the
    /// index has at least one expression key). The many column-only consumers â€” the GIN/GiST
    /// entry builders, FK-target matching, introspection â€” use this; an expression key makes the
    /// index ineligible for those (an FK cannot target an expression index, etc.).
    pub fn column_ordinals(&self) -> Option<Vec<usize>> {
        self.keys.iter().map(IndexKey::as_column).collect()
    }

    /// The column ordinal of the first key element, assuming a single-column index â€” used by the
    /// GIN/GiST paths, which are always single-column plain-column indexes (this slice).
    pub fn first_column(&self) -> usize {
        self.keys[0]
            .as_column()
            .expect("GIN/GiST index key is a plain column")
    }
}

/// The persisted referential action for a foreign key's `ON DELETE` / `ON UPDATE`
/// (spec/design/constraints.md Â§6.6). Only `NoAction` (the default) and `Restrict` are
/// supported â€” they are identical in jed (no deferrable constraints). The write-actions
/// (CASCADE / SET NULL / SET DEFAULT) are rejected `0A000` at CREATE TABLE, so never reach here;
/// the on-disk encoding reserves codes for them (format.md).
#[derive(Clone, Copy, PartialEq, Eq, Debug)]
pub enum FkAction {
    NoAction,
    Restrict,
}

/// One resolved `FOREIGN KEY` constraint of a table (spec/design/constraints.md Â§6): its
/// (per-table constraint-namespace) name, the referencing column ordinals into **this** table in
/// list order, the referenced (parent) table name, the referenced column ordinals into the
/// **parent** in list order (same length as `columns`), and the referential actions. An FK owns no
/// B-tree; enforcement probes the parent's PK store or a unique index (Â§6.4). Held in ascending
/// lowercased-name order on the table (the catalog's on-disk order and the child-side evaluation
/// order â€” Â§6.9).
#[derive(Clone, PartialEq, Eq, Debug)]
pub struct ForeignKeyConstraint {
    pub name: String,
    pub columns: Vec<usize>,
    pub ref_table: String,
    pub ref_columns: Vec<usize>,
    pub on_delete: FkAction,
    pub on_update: FkAction,
}

/// One element's operator in an `EXCLUDE` constraint (spec/design/gist.md Â§7) â€” the `WITH` operator
/// of a `(column WITH op)` pair. `Overlaps` is `&&` (a range column, `range_ops`); `Equal` is `=` (a
/// fixed-width keyable scalar, the in-core `btree_gist`). Both are **symmetric**, the property an
/// exclusion operator must have. Persisted as the v21 per-element strategy byte (format.md).
#[derive(Clone, Copy, PartialEq, Eq, Debug)]
pub enum ExclusionOp {
    /// `&&` â€” range overlap.
    Overlaps,
    /// `=` â€” scalar equality.
    Equal,
}

/// One `(column, operator)` element of an `EXCLUDE` constraint â€” a member column ordinal into the
/// owning table and its `WITH` operator. The element order matches the backing GiST index's column
/// order (spec/design/gist.md Â§7/Â§8).
#[derive(Clone, Copy, PartialEq, Eq, Debug)]
pub struct ExclusionElement {
    pub column: usize,
    pub op: ExclusionOp,
}

/// One `EXCLUDE` constraint (spec/design/gist.md Â§7): its (relation-namespace) name, the backing
/// **GiST** index that enforces it (by name â€” the constraint *is* its index, the UNIQUE-is-its-index
/// model generalized from a single implicit `=` to an explicit operator list), and the
/// `(column, operator)` element vector the conjunction probe needs. Held in ascending
/// lowercased-name order on the table (the catalog's on-disk order). The NULL rule (Â§7) is enforced
/// at probe time: a row with a NULL in any excluded column never conflicts.
#[derive(Clone, PartialEq, Eq, Debug)]
pub struct ExclusionConstraint {
    pub name: String,
    /// The backing GiST index's name (equal to `name` for a GX3 constraint â€” it owns the index).
    pub index: String,
    pub elements: Vec<ExclusionElement>,
}

/// A user-defined **composite (row) type** (spec/design/composite.md): a named, ordered list of
